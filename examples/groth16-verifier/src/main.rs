use groth16_verifier_methods::{INPUTS_TEST_PUB_INP_ELF, INPUTS_TEST_PUB_INP_ID};
use groth16_verifier_methods::{INPUTS_TEST_SEAL_ELF, INPUTS_TEST_SEAL_ID};
