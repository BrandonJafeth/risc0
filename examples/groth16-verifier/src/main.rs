const PROOF: &str = include_str!("data/proof.json");
const PUBLIC_INPUTS: &str = include_str!("data/public.json");
const VERIFICATION_KEY: &str = include_str!("data/verification_key.json");

fn inputs_test_pub_inp(pub_inp: &Vec<Fr>) {
    let env = ExecutorEnv::builder()
        .write(&pub_inp)
        .unwrap()
        .build()
        .unwrap();

    // We run the prover to generate a receipt of correct verification
    let receipt = default_prover()
        .prove(env, INPUTS_TEST_PUB_INP_ELF)
        .unwrap()
        .receipt;

    // We verify the final receipt, which recursively verifies the Groth16 proof.
    receipt.verify(INPUTS_TEST_PUB_INP_ID).unwrap();

    let new_pub_inp: Vec<Fr> = receipt.journal.decode().unwrap();
    assert_eq!(new_pub_inp.len(), pub_inp.len());
    for (lhs, rhs) in new_pub_inp.iter().zip(pub_inp.iter()) {
        assert_eq!(lhs, rhs);
    }
    // assert!(false, "TODO: Aborting early to clarify where the failure is; this is the end of `inputs_test_pub_inp`");
}

